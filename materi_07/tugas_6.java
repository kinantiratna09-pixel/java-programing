package materi_07;

public class tugas_6 {
    // mendefinisikan kelas publik bernama tugas_6
    public static void main(String[] args) {
        // metode utama yang akan di eksekusi saat program dijalankan

        for (char i = 'A'; i <= 'Z'; i++) {
            // perulangan for untuk mencetak huruf dari A sampai Z
            System.out.print(i + " ");
            // mencetak huruf i diikuti dengan spasi kosong
        }
    }
}
