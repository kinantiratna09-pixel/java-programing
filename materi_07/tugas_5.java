package materi_07;

public class tugas_5 {
    // mendefinisikan kelas publik bernama tugas_5
    public static void main(String[] args) {
        // metode utama yang akan di eksekusi saat program dijalankan
        System.out.println(" BIANGAN GANJIL:");
        // mencetak pesan "BILANGAN GANJIL:"
        for (int i = 1; i <= 100; i++) {
            // perulangan for untuk mencetak bilangan ganjil dari 1 sampai 100
            if (i % 2 == 1) {
                // percabangan if untuk mengecek apakah i adalah bilangan ganjil
                System.out.print(i + " ");
                // mencetak bilangan ganjil diikuti dengan spasi kosong
            }
        }
        }
    }
