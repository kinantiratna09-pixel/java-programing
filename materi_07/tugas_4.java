package materi_07;

public class tugas_4 {
    // mendefinisikan kelas publik bernama tugas_4
    public static void main(String[] args) {
        // metode utama yang akan di eksekusi saat program dijalankan
        System.out.println("BILANGAN GENAP:");
        // mencetak pesan "BILANGAN GENAP:"
        for (int i = 1; i <= 100; i++) {
            // perulangan for untuk mencetak bilangan genap dari 1 sampai 100
            if (i % 2 == 0) {
                // percabangan if untuk mengecek apakah i adalah bilangan genap
                System.out.print(i + " ");
                // mencetak bilangan genap diikuti dengan spasi kosong
            }
        }
        }
    }
